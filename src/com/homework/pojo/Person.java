package com.homework.pojo;

public class Person {
	private int id;
	private String personName;
	private String personClass;
	private String personText;
	private String personFingerPrint;
	private String personFingerPrintHash;
	private String personFingerPrintInsertTime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getPersonClass() {
		return personClass;
	}

	public void setPersonClass(String personClass) {
		this.personClass = personClass;
	}

	public String getPersonText() {
		return personText;
	}

	public void setPersonText(String personText) {
		this.personText = personText;
	}

	public String getPersonFingerPrint() {
		return personFingerPrint;
	}

	public void setPersonFingerPrint(String personFingerPrint) {
		this.personFingerPrint = personFingerPrint;
	}

	public String getPersonFingerPrintHash() {
		return personFingerPrintHash;
	}

	public void setPersonFingerPrintHash(String personFingerPrintHash) {
		this.personFingerPrintHash = personFingerPrintHash;
	}

	public String getPersonFingerPrintInsertTime() {
		return personFingerPrintInsertTime;
	}

	public void setPersonFingerPrintInsertTime(String personFingerPrintInsertTime) {
		this.personFingerPrintInsertTime = personFingerPrintInsertTime;
	}
}
